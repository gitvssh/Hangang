package com.example.administrator.hangang.database;

public class Program {
    String id;
    String title;
    String content;
    String date;
    String place;
    String participate;
    String fee;

    public Program(String id, String title, String content, String date, String place, String participate, String fee){
        this.id=id;
        this.title=title;
        this.content=content;
        this.date=date;
        this.place=place;
        this.participate=participate;
        this.fee=fee;
    }
}
